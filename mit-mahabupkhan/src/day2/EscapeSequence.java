package day2;

public class EscapeSequence {
public static void main(String[] args) {
	System.out.println("hello \"firnas\" world");
	
	System.out.println("hello \'firnas\' world");
	
	System.out.println("hello \nfirnas\n world");
	
	System.out.println("hello \tfirnas\t world");
	
	System.out.println("hello \t\tfirnas\t\t world");
	
	System.out.println("hello \n\tfirnas\n\t\t world");
	
	System.out.println("hello \b\b\b world");
	
	System.out.println("hello hello\rworld");
	
}
}
